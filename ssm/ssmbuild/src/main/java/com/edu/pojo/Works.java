package com.edu.pojo;

public class Works {
    private String Pno;
    private String Eno;
    private int Hours;

    public String getPno() {
        return Pno;
    }

    public void setPno(String pno) {
        Pno = pno;
    }

    public String getEno() {
        return Eno;
    }

    public void setEno(String eno) {
        Eno = eno;
    }

    public int getHours() {
        return Hours;
    }

    public void setHours(int hours) {
        Hours = hours;
    }
}
